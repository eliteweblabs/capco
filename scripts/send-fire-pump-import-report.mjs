#!/usr/bin/env node
/**
 * Email aggregated Fire Pump customer import report to Admin users.
 * Usage: node scripts/send-fire-pump-import-report.mjs [--base-url http://localhost:4321]
 */
import { createClient } from "@supabase/supabase-js";
import { readFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const baseUrl = process.argv.includes("--base-url")
  ? process.argv[process.argv.indexOf("--base-url") + 1]
  : process.env.BASE_URL || "http://127.0.0.1:4321";

function loadEnv() {
  const env = {};
  try {
    const raw = readFileSync(resolve(root, ".env"), "utf8");
    for (const line of raw.split("\n")) {
      if (!line || line.startsWith("#")) continue;
      const i = line.indexOf("=");
      if (i < 0) continue;
      const key = line.slice(0, i);
      let val = line.slice(i + 1).trim();
      if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
        val = val.slice(1, -1);
      }
      env[key] = val;
    }
  } catch {
    /* optional */
  }
  return env;
}

function esc(s) {
  return String(s ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function buildHtml(data) {
  const updatedRows = data.updated
    .map(
      (r) =>
        `<tr><td>${r.n}</td><td>${esc(r.company)}</td><td>${esc(r.email)}</td><td>${esc(r.phone || "—")}</td></tr>`
    )
    .join("");
  const pendingRows = data.placeholders
    .map(
      (r, i) =>
        `<tr><td>${i + 4}</td><td>${esc(r.companyName)}</td><td>${esc(r.email)}</td><td>${esc(r.phone || "—")}</td></tr>`
    )
    .join("");
  const deferredBlocks = (data.deferred || [])
    .map((g) => {
      const projects = g.projects
        .map((p) => `<li><b>${esc(p.spreadsheetName)}</b>${p.phone ? ` — ${esc(p.phone)}` : ""}</li>`)
        .join("");
      return `<p><b>${esc(g.clientEmail)}</b> (client: ${esc(g.canonicalSpreadsheetName)})<ul>${projects}</ul></p>`;
    })
    .join("");

  return `<!DOCTYPE html><html><body style="font-family:sans-serif;line-height:1.5;color:#111">
<h1>Fire Pump Customer Import — Summary</h1>
<p><b>Date:</b> ${esc(new Date().toLocaleString("en-US", { timeZone: "America/New_York" }))} ET<br>
<b>Source:</b> Customers.xlsx (FIRE PUMP TESTING COMPANY, INC.)<br>
<b>Policy:</b> One profile per client email; duplicate spreadsheet rows = projects later.</p>

<h2>Import totals</h2>
<ul>
<li><b>${data.totalClients}</b> Client profiles in Supabase</li>
<li><b>${data.withRealEmail}</b> with real email addresses</li>
<li><b>${data.placeholders.length}</b> still on placeholder email (@fire-pump-import.local)</li>
<li><b>${data.deferredProjectCount}</b> deferred project rows (same email as existing client)</li>
</ul>

<h2>Emails updated (you provided / researched)</h2>
<table border="1" cellpadding="6" cellspacing="0" style="border-collapse:collapse">
<tr><th>#</th><th>Customer</th><th>Email</th><th>Phone</th></tr>
${updatedRows}
</table>

<h2>Still need real email</h2>
<table border="1" cellpadding="6" cellspacing="0" style="border-collapse:collapse">
<tr><th>#</th><th>Customer</th><th>Placeholder</th><th>Phone</th></tr>
${pendingRows}
</table>

<h2>Deferred projects (do not create separate clients)</h2>
${deferredBlocks || "<p>None</p>"}

<p style="margin-top:2em;color:#666;font-size:12px">Generated by scripts/send-fire-pump-import-report.mjs</p>
</body></html>`;
}

async function main() {
  const env = { ...process.env, ...loadEnv() };
  const url = env.PUBLIC_SUPABASE_URL;
  const key = env.SUPABASE_SECRET || env.SUPABASE_SERVICE_ROLE_KEY;
  if (!url || !key) {
    console.error("Missing Supabase env");
    process.exit(1);
  }

  const sb = createClient(url, key, { auth: { persistSession: false, autoRefreshToken: false } });

  const { data: placeholders } = await sb
    .from("profiles")
    .select("companyName, email, phone, firstName, lastName")
    .eq("role", "Client")
    .like("email", "%@fire-pump-import.local")
    .order("companyName");

  const { count: totalClients } = await sb
    .from("profiles")
    .select("*", { count: "exact", head: true })
    .eq("role", "Client");

  const withRealEmail = (totalClients || 0) - (placeholders?.length || 0);

  const updated = [
    {
      n: 1,
      company: "Anthony Barrasso Residence",
      email: "jinxedpirate@gmail.com",
      phone: "(617) 875-9093",
    },
    { n: 2, company: "Ben Brown", email: "info@tbreno.com", phone: null },
    {
      n: 3,
      company: "Casper Plumbing & Heating LLC.",
      email: "casperphdc@gmail.com",
      phone: "(617) 823-8539 (from casperphdc.com)",
    },
  ];

  let deferred = [];
  try {
    const raw = JSON.parse(
      readFileSync(resolve(root, "scripts/data/fire-pump-customers-deferred-projects.json"), "utf8")
    );
    deferred = raw.deferred || [];
  } catch {
    /* optional */
  }

  const deferredProjectCount = deferred.reduce((n, g) => n + (g.projects?.length || 0), 0);

  const payload = {
    totalClients: totalClients || 0,
    withRealEmail,
    placeholders: placeholders || [],
    updated,
    deferred,
    deferredProjectCount,
  };

  const html = buildHtml(payload);
  const reportPath = resolve(root, "markdowns/fire-pump-customer-import-report.md");
  const md = `# Fire Pump Customer Import — Summary

**Date:** ${new Date().toISOString()}  
**Source:** Customers.xlsx (FIRE PUMP TESTING COMPANY, INC.)

## Totals

- **${payload.totalClients}** Client profiles
- **${payload.withRealEmail}** with real email
- **${payload.placeholders.length}** placeholder emails remaining
- **${payload.deferredProjectCount}** deferred project rows (same client email)

## Emails updated

| # | Customer | Email | Phone |
|---|----------|-------|-------|
${updated.map((r) => `| ${r.n} | ${r.company} | ${r.email} | ${r.phone || "—"} |`).join("\n")}

## Still need real email

| # | Customer | Placeholder | Phone |
|---|----------|-------------|-------|
${(placeholders || []).map((r, i) => `| ${i + 4} | ${r.companyName} | ${r.email} | ${r.phone || "—"} |`).join("\n")}

## Deferred projects (projects later — not separate clients)

${deferred
  .map(
    (g) =>
      `### ${g.clientEmail} (${g.canonicalSpreadsheetName})\n${(g.projects || [])
        .map((p) => `- **${p.spreadsheetName}**${p.phone ? ` — ${p.phone}` : ""}`)
        .join("\n")}`
  )
  .join("\n\n")}
`;

  const { writeFileSync, mkdirSync } = await import("fs");
  mkdirSync(resolve(root, "markdowns"), { recursive: true });
  writeFileSync(reportPath, md);
  console.log("Wrote:", reportPath);

  const emailPayload = {
    method: "internal",
    rolesToNotify: ["Admin", "superAdmin"],
    usersToNotify: ["mduross@firepumptestingco.com", "sen@eliteweblabs.com"],
    emailSubject: "Fire Pump Customer Import — Summary Report",
    emailContent: html,
    buttonText: "Admin Users",
    buttonLink: "/admin/users",
    trackLinks: false,
  };

  const res = await fetch(`${baseUrl}/api/delivery/update-delivery`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(emailPayload),
  });
  const result = await res.json().catch(() => ({}));

  if (!res.ok) {
    console.error("Delivery API failed:", res.status, result);
    console.log("\nReport saved locally. Start dev server and re-run, or read markdowns/fire-pump-customer-import-report.md");
    process.exit(1);
  }

  console.log("Sent via update-delivery:", result.message || result);
  console.log("totalSent:", result.totalSent);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
