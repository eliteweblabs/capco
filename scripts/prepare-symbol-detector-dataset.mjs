#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import sharp from "sharp";

const ROOT = process.cwd();
const DEFAULT_ANNOTATIONS = path.join(ROOT, "fixtures/drawing-analyzer/training/annotations.json");
const EXAMPLE_ANNOTATIONS = path.join(
  ROOT,
  "fixtures/drawing-analyzer/training/annotations.example.json"
);
const DATASET_ROOT = path.join(ROOT, "ml/symbol-detector/dataset");
const DATA_YAML = path.join(ROOT, "ml/symbol-detector/data.yaml");

function parseArgs() {
  const args = process.argv.slice(2);
  const out = { annotations: DEFAULT_ANNOTATIONS };
  for (let i = 0; i < args.length; i += 1) {
    if (args[i] === "--annotations" && args[i + 1]) {
      out.annotations = path.resolve(ROOT, args[i + 1]);
      i += 1;
    }
  }
  return out;
}

function ensureDir(dirPath) {
  if (!fs.existsSync(dirPath)) fs.mkdirSync(dirPath, { recursive: true });
}

function normalizeSplit(splitRaw) {
  const split = String(splitRaw ?? "train").toLowerCase();
  if (split === "train" || split === "val" || split === "test") return split;
  return "train";
}

function toYoloLine(box, classIndex, imageWidth, imageHeight) {
  const cx = (Number(box.x) + Number(box.width) / 2) / imageWidth;
  const cy = (Number(box.y) + Number(box.height) / 2) / imageHeight;
  const w = Number(box.width) / imageWidth;
  const h = Number(box.height) / imageHeight;
  return `${classIndex} ${cx.toFixed(6)} ${cy.toFixed(6)} ${w.toFixed(6)} ${h.toFixed(6)}`;
}

function writeDataYaml(classes) {
  const yaml = `# Auto-generated by scripts/prepare-symbol-detector-dataset.mjs
path: ${DATASET_ROOT}
train: images/train
val: images/val
test: images/test

names:
${classes.map((name, idx) => `  ${idx}: ${name}`).join("\n")}
`;
  fs.writeFileSync(DATA_YAML, yaml, "utf8");
}

async function main() {
  const args = parseArgs();
  if (!fs.existsSync(args.annotations)) {
    if (args.annotations === DEFAULT_ANNOTATIONS && fs.existsSync(EXAMPLE_ANNOTATIONS)) {
      fs.copyFileSync(EXAMPLE_ANNOTATIONS, DEFAULT_ANNOTATIONS);
      console.warn(`No annotations.json found. Bootstrapped from example: ${DEFAULT_ANNOTATIONS}`);
    } else {
      console.error(`Annotations file not found: ${args.annotations}`);
      console.error(
        "Copy fixtures/drawing-analyzer/training/annotations.example.json to annotations.json and edit it."
      );
      process.exit(1);
    }
  }

  const raw = JSON.parse(fs.readFileSync(args.annotations, "utf8"));
  const classes = Array.isArray(raw.classes) ? raw.classes.map((x) => String(x)) : [];
  const items = Array.isArray(raw.items) ? raw.items : [];

  if (classes.length === 0) {
    console.error("No classes found in annotations file.");
    process.exit(1);
  }
  if (items.length === 0) {
    console.error("No items found in annotations file.");
    process.exit(1);
  }

  const classToIndex = new Map(classes.map((name, idx) => [name, idx]));
  const counts = { train: 0, val: 0, test: 0 };
  let firstPreparedTrain = null;

  for (const split of ["train", "val", "test"]) {
    ensureDir(path.join(DATASET_ROOT, "images", split));
    ensureDir(path.join(DATASET_ROOT, "labels", split));
  }

  for (const item of items) {
    const split = normalizeSplit(item.split);
    const imagePath = path.resolve(ROOT, String(item.imagePath ?? ""));
    const id = String(item.id ?? path.parse(imagePath).name);
    const boxes = Array.isArray(item.boxes) ? item.boxes : [];

    if (!fs.existsSync(imagePath)) {
      console.warn(`Skipping ${id}: image not found at ${imagePath}`);
      continue;
    }

    const ext = path.extname(imagePath).toLowerCase() || ".png";
    const imageOut = path.join(DATASET_ROOT, "images", split, `${id}${ext}`);
    const labelOut = path.join(DATASET_ROOT, "labels", split, `${id}.txt`);

    fs.copyFileSync(imagePath, imageOut);
    const metadata = await sharp(imagePath).metadata();
    const imageWidth = Number(metadata.width ?? 0);
    const imageHeight = Number(metadata.height ?? 0);
    if (!imageWidth || !imageHeight) {
      console.warn(`Skipping ${id}: could not read dimensions.`);
      continue;
    }

    const yoloLines = [];
    for (const box of boxes) {
      const cls = String(box.class ?? "");
      const classIndex = classToIndex.get(cls);
      if (classIndex == null) {
        console.warn(`Skipping unknown class "${cls}" in ${id}`);
        continue;
      }
      yoloLines.push(toYoloLine(box, classIndex, imageWidth, imageHeight));
    }
    fs.writeFileSync(labelOut, yoloLines.join("\n"), "utf8");
    counts[split] += 1;
    if (split === "train" && !firstPreparedTrain) {
      firstPreparedTrain = { imageOut, labelOut, ext };
    }
  }

  if (counts.val === 0 && firstPreparedTrain) {
    const fallbackId = "__auto_val_from_train";
    const valImageOut = path.join(DATASET_ROOT, "images", "val", `${fallbackId}${firstPreparedTrain.ext}`);
    const valLabelOut = path.join(DATASET_ROOT, "labels", "val", `${fallbackId}.txt`);
    fs.copyFileSync(firstPreparedTrain.imageOut, valImageOut);
    fs.copyFileSync(firstPreparedTrain.labelOut, valLabelOut);
    counts.val += 1;
    console.warn(
      "No val items provided. Auto-created one validation sample from train split for YOLO compatibility."
    );
  }

  writeDataYaml(classes);
  console.log("Prepared symbol detector dataset.");
  console.log(`Annotations: ${args.annotations}`);
  console.log(`Dataset root: ${DATASET_ROOT}`);
  console.log(`Data YAML: ${DATA_YAML}`);
  console.log(`Images per split: train=${counts.train}, val=${counts.val}, test=${counts.test}`);
}

main().catch((error) => {
  console.error("Failed to prepare dataset:", error);
  process.exit(1);
});
