// Helper function to get Lucide icon name from BoxIcons name
export function getLucideIconName(boxIconName: string): string {
  // Remove 'bx-' prefix if present
  const cleanName = boxIconName.replace(/^bx-/, "");
  return iconMapping[`bx-${cleanName}`] || iconMapping[boxIconName] || cleanName;
}
